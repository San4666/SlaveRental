<?php
namespace SlaveRental\Entity;

class Category
{
    /** @var string */
    private $name;

    /** @var Category|null */
    private $parent;

    public function getParent(): ?Category
    {
        return $this->parent;
    }

    public function setParent(?Category $parent): self
    {
        $this->parent = $parent;

        return $this;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function setName(string $name): self
    {
        $this->name = $name;

        return $this;
    }

}